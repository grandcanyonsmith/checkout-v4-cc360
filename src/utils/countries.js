// Function to get user's country based on IP
export const getUserCountry = async () => {
  try {
    const response = await fetch('https://ipapi.co/json/')
    const data = await response.json()
    return data.country_code
  } catch (error) {
    console.error('Failed to get user country:', error)
    return 'US' // Default to US if geolocation fails
  }
}

// Function to format phone number based on country
export const formatPhoneNumber = (phoneNumber, countryCode) => {
  // Remove all non-digits
  const digits = phoneNumber.replace(/\D/g, '')
  
  // Apply different formatting based on country
  switch (countryCode) {
    case 'US':
    case 'CA':
      // US/Canada format: (123) 456-7890
      if (digits.length <= 3) return digits
      if (digits.length <= 6) return `(${digits.slice(0, 3)}) ${digits.slice(3)}`
      return `(${digits.slice(0, 3)}) ${digits.slice(3, 6)}-${digits.slice(6, 10)}`
    
    case 'GB':
      // UK format: 020 1234 5678
      if (digits.length <= 3) return digits
      if (digits.length <= 7) return `${digits.slice(0, 3)} ${digits.slice(3)}`
      return `${digits.slice(0, 3)} ${digits.slice(3, 7)} ${digits.slice(7, 11)}`
    
    case 'DE':
    case 'FR':
    case 'ES':
    case 'IT':
      // European format: 12 34 56 78 90
      if (digits.length <= 2) return digits
      if (digits.length <= 4) return `${digits.slice(0, 2)} ${digits.slice(2)}`
      if (digits.length <= 6) return `${digits.slice(0, 2)} ${digits.slice(2, 4)} ${digits.slice(4)}`
      if (digits.length <= 8) return `${digits.slice(0, 2)} ${digits.slice(2, 4)} ${digits.slice(4, 6)} ${digits.slice(6)}`
      return `${digits.slice(0, 2)} ${digits.slice(2, 4)} ${digits.slice(4, 6)} ${digits.slice(6, 8)} ${digits.slice(8, 10)}`
    
    default:
      // Generic international format: just add spaces every 3-4 digits
      if (digits.length <= 3) return digits
      if (digits.length <= 6) return `${digits.slice(0, 3)} ${digits.slice(3)}`
      if (digits.length <= 9) return `${digits.slice(0, 3)} ${digits.slice(3, 6)} ${digits.slice(6)}`
      return `${digits.slice(0, 3)} ${digits.slice(3, 6)} ${digits.slice(6, 9)} ${digits.slice(9)}`
  }
}

// Function to validate phone number based on country
export const validatePhoneNumber = (phoneNumber, countryCode) => {
  const digits = phoneNumber.replace(/\D/g, '')
  
  switch (countryCode) {
    case 'US':
    case 'CA':
      return digits.length === 10
    case 'GB':
      return digits.length >= 10 && digits.length <= 11
    case 'DE':
      return digits.length >= 10 && digits.length <= 12
    case 'FR':
      return digits.length === 10
    case 'AU':
      return digits.length === 9 || digits.length === 10
    default:
      return digits.length >= 7 && digits.length <= 15
  }
} 